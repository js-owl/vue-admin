<template>
  <div class="sidebar-logo-container" :class="{ collapse: collapse }">
    <transition name="sidebarLogoFade">
      <router-link
        v-if="collapse"
        key="collapse"
        class="sidebar-logo-link"
        to="/"
      >
        <img v-if="logo" :src="logo" class="sidebar-logo" />
        <h1 v-else class="sidebar-title">ЭЛИС {{ role_name }}</h1>
      </router-link>
      <router-link v-else key="expand" class="sidebar-logo-link" to="/">
        <img v-if="logo" :src="logo" class="sidebar-logo" />
        <h1 class="sidebar-title">ЭЛИС {{ role_name_ru }}</h1>
      </router-link>
    </transition>
  </div>
</template>

<script>
import logoImg from '@/assets/img/logo.png'
import { mapGetters } from 'vuex'
export default {
  name: 'SidebarLogo',
  props: {
    collapse: {
      type: Boolean,
      required: true
    }
  },
  data() {
    return {
      logo: logoImg
    }
  },
  computed: {
    ...mapGetters(['role_name']),
    role_name_ru() {
      switch (this.role_name) {
        case 'admin':
          return 'Админ'
        case 'dpo':
          return 'ДПО'
        case 'urrd':
          return 'ОРРД'
        case 'translator':
          return 'Переводчик'
        case 'dialer':
          return 'Дилер'
        default:
          return this.role_name
      }
    }
  }
}
</script>

